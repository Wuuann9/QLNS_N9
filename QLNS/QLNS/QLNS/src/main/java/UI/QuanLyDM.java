    private void hienThiToanBoDanhMuc() {
        DM_Connect nxb_conn = new DM_Connect();
        dsnxbs = nxb_conn.layToanBoDanhMuc();
        dtmDM = new DefaultTableModel();
        dtmDM.addColumn("Mã danh mục");
        dtmDM.addColumn("Tên danh mục");
        dtmDM.addColumn("Mô tả");
        dsnxbs = nxb_conn.layToanBoDanhMuc();
        dtmDM.setRowCount(0);
        for (DM nxb : dsnxbs) {
            Vector<Object> vec = new Vector<Object>();
            vec.add(nxb.getMaDM());
            vec.add(nxb.getTenDM());
            vec.add(nxb.getMoTa());
            dtmDM.addRow(vec);
        }
        DanhMucTable.setModel(dtmDM);
    }

