    private void UpdateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateBtnActionPerformed
        if (MaDMBInput.getText().trim().isEmpty() || TenDMInput.getText().trim().isEmpty() || MoTaInput.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!", "Lỗi", JOptionPane.WARNING_MESSAGE);
            return;
        }

        DM_Connect dm_conn = new DM_Connect();
        DM dm = new DM();
        dm.setMaDM(MaDMBInput.getText().trim());
        dm.setTenDM(TenDMInput.getText().trim());
        dm.setMoTa(MoTaInput.getText().trim());

        int ret = JOptionPane.showConfirmDialog(null, "Bạn muốn chỉnh sửa danh mục?", "Xác nhận chỉnh sửa", JOptionPane.OK_CANCEL_OPTION);
        if (ret == JOptionPane.OK_OPTION) {
            int activeUpdate = dm_conn.updateDM(dm);
            if (activeUpdate > 0) {
                JOptionPane.showMessageDialog(null, "Chỉnh sửa thành công!");
                hienThiToanBoDanhMuc();
            } else if (activeUpdate == 0) {
                JOptionPane.showMessageDialog(null, "Không tìm thấy danh mục cần chỉnh sửa!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Chỉnh sửa thất bại! Kiểm tra lại thông tin.", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_UpdateBtnActionPerformed
    private void xuLyThemMoi(String maDM) throws ParseException {
        DM dm = new DM();
        dm.setMaDM(MaDMBInput.getText());
        dm.setTenDM(TenDMInput.getText());
        dm.setMoTa(MoTaInput.getText());
        DM_Connect nxb_conn = new DM_Connect();
        if (nxb_conn.kiemTraTonTai(maDM) == true) {
            JOptionPane.showMessageDialog(null, "Mã danh mục này đã tồn tại!");
        } else {
            int activeLuu = nxb_conn.ThemMoiDM(dm);
            if (activeLuu > 0) {
                JOptionPane.showMessageDialog(null, "Thêm mới thành công!");
                hienThiToanBoDanhMuc();
            } else {
                JOptionPane.showMessageDialog(null, "Thêm mới thất bại!");
            }
        }
    }
    private void AddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBtnActionPerformed
        int ret = JOptionPane.showConfirmDialog(null, "Bạn muốn thêm danh mục này?", "xác nhận xác nhận để thêm", JOptionPane.OK_CANCEL_OPTION);
        if (ret == JOptionPane.OK_OPTION) {
            if (MaDMBInput.getText().equals("") || TenDMInput.getText().equals("") || MoTaInput.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Dữ liệu còn thiếu");
            } else {
                String maDM = MaDMBInput.getText();
                try {
                    xuLyThemMoi(maDM);
                } catch (ParseException e1) {
                    e1.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_AddBtnActionPerformed

